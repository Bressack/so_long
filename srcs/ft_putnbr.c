#include <utils.h>

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		write(1, "-2147483648", 11);
	if (n < 0 && n > -2147483648)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n < 10 && n > -2147483648)
		write(1, &NUM[n], 1);
	else if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
